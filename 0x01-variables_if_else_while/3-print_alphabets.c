#include <stdio.h>

/**
 * main - prints the alphabet in lowercase, and then in uppercase
 * followed by a new line in the code
 * Returns: Always (0)
 */
int main(void)
{
	char alp[52] = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ";

	int x;

for (x = 0; x < 52; x++);

	{

		putchar(alp[x]);

	}

	putchar('\n');

	return (0);
}
